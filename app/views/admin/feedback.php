  <h3>Getting Started</h3>
  <ol>
    <li>To add testimonials you'll need to go to the <a href="/admin/testimonials/manage">manage tab</a>.
    <br/>Once you create new testimonials, you can send out the forms by sharing the link named "link"
    to the left of each testimonial row.
    <br/>(The automatic collection functionality is offline at the moment.)
    </li>
    
    <li>Make sure to mark "publish" within the testimonial editor when you are ready to publish.</li>
  
    <li>Go to the <a href="/admin/testimonials/display">display tab</a> to see your updated widget and choose a layout.</li>
  
    <li>Grab the code from the <a href="/admin/install/testimonials">install tab</a> and place it on your webiste anywhere you want your testimonials to display.</li>
  </ol>
  
  
